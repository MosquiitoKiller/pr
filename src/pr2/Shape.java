package pr2;

public class Shape {
    String type;
    double s;

    public Shape(String type, double s) {
        this.type = type;
        this.s = s;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public double getS() {
        return s;
    }

    public void setS(double s) {
        this.s = s;
    }


}
